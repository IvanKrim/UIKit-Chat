import UIKit

extension UIFont {
    // настройка для отображаемого текста
    static func arial20() -> UIFont? {
        return UIFont.init(name: "arial", size: 20)
    }
    
    static func arial26() -> UIFont? {
        return UIFont.init(name: "arial", size: 26)
    }
}
