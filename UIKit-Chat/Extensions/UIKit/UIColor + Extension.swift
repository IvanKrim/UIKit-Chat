import UIKit

extension UIColor {

    static func buttonRed() -> UIColor {
        return #colorLiteral(red: 0.8736053109, green: 0, blue: 0.2981623113, alpha: 1)
    }

    static func mainWhite() -> UIColor {
        return #colorLiteral(red: 0.968627451, green: 0.9725490196, blue: 0.9921568627, alpha: 1)
    }

    static func buttonDark() -> UIColor {
        return #colorLiteral(red: 0.2605174184, green: 0.2605243921, blue: 0.260520637, alpha: 1)
    }
    
    static func textFielLight() -> UIColor {
        return #colorLiteral(red: 0.9019607843, green: 0.9019607843, blue: 0.9019607843, alpha: 1)
    }
    
    static func textGrayLight() -> UIColor {
        return #colorLiteral(red: 0.6394036412, green: 0.6394036412, blue: 0.6394036412, alpha: 1)
    }
    
}
